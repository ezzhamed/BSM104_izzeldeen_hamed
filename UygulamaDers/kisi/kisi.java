public class kisi {
    private String isim;
    private int yas;

    public kisi(String isim, int yas){
        this.isim = isim;
        this.yas = yas;
    }
    public String getIsim(){
        return isim;
    }
    public void setIsim(String isim){
        this.isim = isim;
    }
    public int getyas(){
        return yas;
    }
    public void setsas(int yas){
        this.yas = yas;
    }
}